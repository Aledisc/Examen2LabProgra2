    private void CrearUsuarioBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearUsuarioBotonActionPerformed
        // TODO add your handling code here:
        if (gestion.verificarUsuarioUnico(CrearUsernameField.getText())){
            gestion.crearNuevoUsuario(CrearUsernameField.getText(), CrearPasswordField.getText());
            JOptionPane.showMessageDialog(null, "Usuario creado exitosamente");
            PanelCrearUsuario.setVisible(false);
            PanelInicio.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Usuario ya existente");
            CrearUsernameField.setText("");
            CrearPasswordField.setText("");
        }
    }//GEN-LAST:event_CrearUsuarioBotonActionPerformed

